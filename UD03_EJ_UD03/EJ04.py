#Pide al usuario dos números
a = int(input("Escribe un número: "))
b = int(input("Escribe otro número: "))

#Operaciones aritméticas con los dos números introducidos por el usuario
suma = (a + b)
resta = (a - b)
producto = (a * b)
division = (a / b)

#Imprime por pantalla el resultado de las operaciones aritméticas
print("Suma : ", suma, "Resta: ", resta, "Producto: ", producto, "División: ", division)